/**
 * From anonymous Class to Lamda expression
 */

package java_lamda_expression;

import java.util.ArrayList;
import java.util.List;
import java.util.Random;

public class FromAnonymousToLamdaExpressionTest {
	public static void main(String[] args) {
		Random r = new Random();
		List<Hero> heros = new ArrayList<Hero>();
		for (int i = 0; i < 5; i++) {
			heros.add(new Hero("hero " + i, r.nextInt(1000), r.nextInt(100)));
		}
		System.out.println("初始化后的集合：");
		System.out.println(heros);
		System.out.println("使用匿名类的方式，筛选出 hp>100 && damange<50的英雄");
		// 匿名类的正常写法
		HeroChecker c1 = new HeroChecker() {
			@Override
			public boolean test(Hero h) {
				return (h.hp > 100 && h.damage < 50);
			}
		};
		System.out.println("匿名类的正常写法:");
		filter(heros,c1);
		System.out.println("\n");
		// 把new HeroChcekcer，方法名，方法返回类型信息去掉
		// 只保留方法参数和方法体
		// 参数和方法体之间加上符号 ->
		HeroChecker c2 = (Hero h) -> {
			return h.hp > 100 && h.damage < 50;
		};
		System.out.println("把new HeroChcekcer方法名和方法返回类型信息去掉,只保留方法参数和方法体参数和方法体之间加上符号 ->:");
		filter(heros,c2);
		System.out.println("\n");
		// 把return和{}去掉
		HeroChecker c3 = (Hero h) -> h.hp > 100 && h.damage < 50;
		System.out.println("把return和{}去掉:");
		filter(heros,c3);
		System.out.println("\n");
		// 把 参数类型和圆括号去掉
		HeroChecker c4 = h -> h.hp > 100 && h.damage < 50;
		System.out.println("把 参数类型和圆括号去掉:");
		filter(heros,c4);
		System.out.println("\n");
		// 直接把表达式传递进去
		System.out.println("直接把表达式传递进去:");
		filter(heros, h -> h.hp > 100 && h.damage < 50);
		System.out.println("\n");
	}
	private static void filter(List<Hero> heros, HeroChecker checker) {
		for (Hero hero : heros) {
			if (checker.test(hero))
			System.out.print(hero);
		}
	}
}
